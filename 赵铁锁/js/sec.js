$(function(){
	function start(){
		$(".wb_l ul").animate({"top":"-30px"},1000,function(){
			var sd=$(".wb_l ul li").eq(0);
			$(".wb_l ul").append(sd);
			$(".wb_l ul").css("top",0);
		});
	}
	setInterval(start,3000);
	$(".wb_r").hover(function(){
		$(".wb_r p").slideDown();
	},function(){
		$(".wb_r p").slideUp();
	});
});
	